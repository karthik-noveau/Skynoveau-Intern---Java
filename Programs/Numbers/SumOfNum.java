public class SumOfNum{
    public static void main(String[] args){

       int last; //declare or define 
       last = 15 ; // initialazation

        int result = 0;   
        for(int i=10; i<=last; i++){ 
            result = result + i ;   
        }
        System.out.println("sum of N number is " + result);
    }
}
